static void WriteData(unsigned char data, unsigned char C)
{
    unsigned char i,dat;                                //i用于循环写入8位数据，dat用于缓存数据
    CS_0();                                                           //使能片选
    if(C==1)                                                        //判断要写入的是指令还是数据
    {
        CD_1();                                                     //等于1 就是数据
    }
    else
    {
        CD_0();                                                     //等于0 就是指令
    }
    dat=data;
    for(i=0;i<8;i++)                                        //循环发送8位数据
    {
        if((dat&0x80)==0x80)                            //判断最高位是否为1
        {
            SDA_1();                                                //为1 拉高总线写入1
        }
        else
        {
            SDA_0();                                                //为0 拉低总线写入0
        }
        SCK_0();                                                    //产生一个时钟
        Delay(5);

        SCK_1();                                                    //时钟结束
        Delay(5);

        dat<<=1;                                                    //左移一位 准备写入下一位
    }
    CS_1();                                                         //写入完毕，取消片选
}

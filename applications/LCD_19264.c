void lcd_show(uint8_t y,uint8_t l,uint8_t list[20],uint8_t len,uint8_t reverse){
    uint8_t i,Centered;
    if(l == 200){
        Centered = 0;
        for(i=0;i<len;i++){
            if(list[i+1] < 100)Centered += 8;
            else Centered += 4;
        }
        if(Centered < 96)l = 96 - Centered;
        else l = 0;
    }
    Centered = 0;
    for(i=0;i<len;i++){
        if((reverse >= 10 && (reverse-10) == i) || reverse == 1){
            if(list[i+1] < 100)lcd_chinese(y, l+Centered, list[i+1], 1),Centered+=16;
            else lcd_number(y, l+Centered, list[i+1]-100, 1),Centered+=8;
        }else {
            if(list[i+1] < 100)lcd_chinese(y, l+Centered, list[i+1], 0),Centered+=16;
            else lcd_number(y, l+Centered, list[i+1]-100, 0),Centered+=8;
        }

    }
}

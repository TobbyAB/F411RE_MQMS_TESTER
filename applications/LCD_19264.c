void Init_lcd(void)
{
    unsigned int i=0;                                       //用于循环写入指令

    lcd_gpio_init();

    RST_0();                                                        //复位显示屏
    rt_thread_mdelay(15);
    RST_1();                                                        //复位完成
    rt_thread_mdelay(30);                                                //注意：此延时必须要有且保持5毫秒左右

    for(i=0;i<8;i++)                                      //循环写入初始化指令
    {
        WriteData(IC[i],0);                             //写入指令
    }

    Dispgraphic(0x00);                                  //显示屏清屏

}
